CREATE PROCEDURE PSS_ACCOUNT_BY_EMAIL
    @ACC_EMAIL VARCHAR(150)
AS
BEGIN
    SELECT
        ACC_ID,
        ACC_EMAIL,
        ACC_PASSWORD,
        ACC_ROL_ID
    FROM ACCOUNT
    WHERE ACC_EMAIL = @ACC_EMAIL
    ORDER BY ACC_ID ASC
END

CREATE PROCEDURE PSI_ACCOUNT
    @ACC_EMAIL VARCHAR(150),
    @ACC_PASSWORD VARCHAR(255),
    @ACC_ROL_ID INT
AS
BEGIN
    INSERT INTO ACCOUNT (ACC_EMAIL, ACC_PASSWORD, ACC_ROL_ID)
    VALUES (@ACC_EMAIL, @ACC_PASSWORD, @ACC_ROL_ID);
END

CREATE PROCEDURE PSS_ACCOUNT
AS
BEGIN
    SELECT
        ACC_ID,
        ACC_EMAIL,
        ACC_PASSWORD,
        ACC_ROL_ID
    FROM ACCOUNT
    ORDER BY ACC_EMAIL ASC
END

CREATE PROCEDURE PSU_ACCOUNT
    @ACC_ID INT,
    @ACC_EMAIL VARCHAR(150),
    @ACC_PASSWORD VARCHAR(255),
    @ACC_ROL_ID INT
AS
BEGIN
    UPDATE ACCOUNT
    SET
        ACC_EMAIL = @ACC_EMAIL,
        ACC_PASSWORD = @ACC_PASSWORD,
        ACC_ROL_ID = @ACC_ROL_ID
    WHERE ACC_ID = @ACC_ID
END

CREATE PROCEDURE PSD_ACCOUNT
    @ACC_ID INT
AS
BEGIN
    DELETE FROM ACCOUNT
    WHERE ACC_ID = @ACC_ID
END